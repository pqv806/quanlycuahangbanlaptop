    public void laysodong() {
        try {
            String sql = "SELECT COUNT(*) FROM dbo.nhacungcap";
            ResultSet rs = KetNoi.Select(sql);
            while (rs.next()) {
                count = rs.getLong(1);
            }

        } catch (Exception e) {
        }
    }

